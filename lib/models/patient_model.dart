class Patient{
  final String name;
  final int age;
  final int dailySteps;
  final String activityLevel;
  final String goal;
  final String physicalLimitation;
  final String discType;

  Patient({
    required this.name,
    required this.age,
    required this.dailySteps,
    required this.activityLevel,
    required this.goal,
    required this.physicalLimitation,
    required this.discType,
  });
}